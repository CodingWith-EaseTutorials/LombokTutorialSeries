package getter_setter;

public class WithoutLombok {
	private int myInt;
	private String myString;
	
	public void setMyInt(int myInt) {
		this.myInt = myInt;
	}
	public int getMyInt() {
		return myInt;
	}
	
	public void setMyString(String myString) {
		this.myString = myString;
	}
	public String getMyString() {
		return myString;
	}
}
